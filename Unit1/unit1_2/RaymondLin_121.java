package unit1_2;
/*File: RaymondLin_121.java
This program swaps two integers a,b using arithmetic operations
*/

public class RaymondLin_121
{
	public static void main(String[] args)
	{
		int a = 7;
		int b = 3;
		System.out.println(a);
		System.out.println(b);
		a = b;
		b = b + 4;
		System.out.println(a);
		System.out.println(b);
	} //end of main
}